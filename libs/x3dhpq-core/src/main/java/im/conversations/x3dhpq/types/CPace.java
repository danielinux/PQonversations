    // Returns true iff pt is one of the 7 forbidden low-order Curve25519 points.
    // Uses Arrays.equals per point; this is a linear scan (7 comparisons × 32 bytes).
    public static boolean isLowOrderPoint(byte[] pt) {
        if (pt == null || pt.length != 32) return false;
        for (byte[] lop : LOW_ORDER_POINTS) {
            if (Arrays.equals(pt, lop)) return true;
        }
        return false;
    }
